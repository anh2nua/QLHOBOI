    private void Ticket() {
        try {
            Integer.parseInt(txtVeNguoiLon.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Số lượng vé là số");
            return;
        }
        try {
            Integer.parseInt(txtVeTreEm.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Số lượng vé là số");
            return;
        }
        if (!checktxt()) {
            JOptionPane.showMessageDialog(rootPane, "Hãy điền 0 vào ô còn trống)");
            return;
        }
        int slVeTreEm = Integer.parseInt(txtVeTreEm.getText());
        int slVeNguoiLon = Integer.parseInt(txtVeNguoiLon.getText());

        int tong = slVeNguoiLon + slVeTreEm;
        int tt = slVeTreEm * 150000 + slVeNguoiLon * 200000;
        lblSLV.setText(String.valueOf(tong));
        lblThanhTien.setText(String.valueOf(tt));
    }

    private boolean checktxt() {
        if (txtVeNguoiLon.getText().isEmpty()) {
            return false;
        } else if (txtVeTreEm.getText().isEmpty()) {
            return false;
        } else {
            return true;
        }
    }

    private void dongHo() {
        Thread t = new Thread() {
            String text;
            SimpleDateFormat sdf = new SimpleDateFormat("hh:mm:ss aa");

            @Override
            public void run() {
                while (true) {
                    Date d = new Date(); //Tạo đối tượng Date để lấy giờ hệ thống
                    text = sdf.format(d);
                    lblDongHo.setText(text);
                    try {
                        Thread.sleep(1000);
                    } catch (Exception e) {
                        break;
                    }
                }
            }
        };
        t.start();
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        Date date = new Date();
        String dateFormat = formatter.format(date);
        lblDate.setText(dateFormat);
    }
