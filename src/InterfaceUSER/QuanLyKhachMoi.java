    private void btnTaoMoi2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoMoi2ActionPerformed
        // TODO add your handling code here:
        try {
            int ngay, thang, nam;
            String day = lblDate.getText();
            ngay = Integer.parseInt(day.substring(0, 2));
            thang = Integer.parseInt(day.substring(3, 5));
            nam = Integer.parseInt(day.substring(6, 10));
            String datee = thang + "-" + ngay + "-" + nam;

            String sql = "insert into HOADONKHACHMOI(maloaidoanhthu,username,thanhtien,thoigian) values(?,?,?,?)";
            PreparedStatement st = k.cn.prepareStatement(sql);
            st.setString(1, "DoanhThuKhachMoi");
            st.setString(2, lblUser.getText());
            st.setInt(3, 0);
            st.setString(4, datee);
            st.executeUpdate();
            loadMaHD();
            JOptionPane.showMessageDialog(rootPane, "Bạn vừa tạo thành công hoá đơn : " + lblMaHD.getText());
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            System.out.println("Tạo hoá đơn không thành công");
        }
    }//GEN-LAST:event_btnTaoMoi2ActionPerformed
    private void loadMaHD() {
        try {
            String sql = "select TOP(1) MAHOADONKM from HOADONKHACHMOI order by MAHOADONKM DESC";
            Statement st = k.cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                lblMaHD.setText(rs.getString(1));
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

