    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            if (!checkNull()) {
                return;
            }
            if (!checkDup()) {
                return;
            }
            String sql = "insert into taikhoan values(?,?,?)";
            PreparedStatement st = k.cn.prepareStatement(sql);
            st.setString(1, txtUser.getText());
            st.setString(2, txtPass.getText());
            String role = null;
            if (rdoQuanLy.isSelected()) {
                role = "quanly";
            } else {
                role = "nhanvien";
            }
            st.setString(3, role);
            st.executeUpdate();
            loadDataToTable();

        } catch (SQLException e) {
            System.out.println(e);

        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            if (!checkNull()) {
                return;
            }
            String sql = "Update taikhoan set passwordd = ?, rolee = ? where username = ?";
            PreparedStatement st = k.cn.prepareStatement(sql);
            st.setString(1, txtPass.getText());
            st.setString(3, txtUser.getText());
            String role = null;
            if (rdoQuanLy.isSelected()) {
                role = "quanly";
            } else {
                role = "nhanvien";
            }
            st.setString(2, role);
            if (tblTaiKhoan.getValueAt(index, 0).toString().equalsIgnoreCase(txtUser.getText())) {
                st.executeUpdate();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Username phải giống trong bảng");
            }
            loadDataToTable();
        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

