    private void loadDataToTable() {
        try {
            model = (DefaultTableModel) tblTaiKhoan.getModel();
            model.setRowCount(0);
            Statement st = k.cn.createStatement();
            ResultSet rs = st.executeQuery("select * from TAIKHOAN");
            while (rs.next()) {
                Vector row = new Vector();
                row.add(rs.getString(1));
                row.add(rs.getString(2));
                row.add(rs.getString(3));
                model.addRow(row);
            }
        } catch (SQLException e) {
            System.out.println("Lỗi tải dữ liệu");
        }
    }

    private void showDetail(int index) {
        txtUser.setText(tblTaiKhoan.getValueAt(index, 0).toString());
        txtPass.setText(tblTaiKhoan.getValueAt(index, 1).toString());
        if (tblTaiKhoan.getValueAt(index, 2).toString().equalsIgnoreCase("quanly")) {
            rdoQuanLy.setSelected(true);
        } else {
            rdoNhanVien.setSelected(true);
        }
        tblTaiKhoan.setRowSelectionInterval(index, index);
    }

    private void clearForm() {
        showDetail(0);
        txtUser.setText("");
        txtPass.setText("");
    }

    private boolean checkNull() {
        if (txtUser.getText().isEmpty()) {
            txtUser.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Username trống");
            return false;
        } else if (txtPass.getText().isEmpty()) {
            txtPass.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu trống");
            return false;
        } else {
            return true;
        }
    }
    
     private boolean checkDup() {
        if (tblTaiKhoan.getValueAt(index, 0).toString().equalsIgnoreCase(txtUser.getText())) {
            txtUser.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Username đã tồn tại");
            return false;
        } else {
            return true;
        }
    }
     
